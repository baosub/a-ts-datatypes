// Tipo: number
const age: number = 25;
console.log(age);

// Tipo: string
const nameMain: string = "Baub";
console.log(nameMain);

// Tipo: boolean
const isStudent: boolean = true;
console.log(isStudent);

// Tipo: null
const emptyValue: null = null;
console.log(emptyValue);

// Tipo: undefined
let notDefined: undefined = undefined;
console.log(notDefined);

// Tipo: any (puede ser cualquier tipo)
let randomValue: any = "Puede ser cualquier cosa";
console.log(randomValue);

// Cambiar el valor de any a otro tipo
randomValue = 42;
console.log(randomValue);
