type Person = {
    name: string;
    age: number;
  };
  
  // Creamos una variable de tipo Person y asignamos un objeto que coincide con la estructura
  const person1: Person = {
    name: "Baub",
    age: 35,
  };
  
  // Imprimimos la variable en la consola
  console.log(person1);
  
  // Imprimimos las propiedades individuales
  console.log(`Nombre: ${person1.name}`);
  console.log(`Edad: ${person1.age}`);

